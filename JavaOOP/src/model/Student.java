package model;

public class Student {

    private String stno;
    private String name;
    private int age;
    private String sex;

    public String getStno() {
        return stno;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getSex() {
        return sex;
    }

    public void setStno(String stno) {
        this.stno = stno;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
